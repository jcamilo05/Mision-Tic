    public void FillBillProducts(String code){
        modelBill = (DefaultTableModel) tbProductBill.getModel();
        CleanTable(modelBill);
        for(clsOrder ord :orderObjList ){
           if(ord.getCustomer().getCustomerCode().equals(code)){
               for(String product: ord.getProductList()){
                   System.out.println(product);
                    modelBill.insertRow(modelBill.getRowCount(),new String[]{product});}
               break;
           }
        }
    }

     public void FillBillCustomer(clsCustomer customer){
               modeCustomerBill = (DefaultTableModel) tbCustomerBill.getModel();
               CleanTable(modeCustomerBill);
               String address = customer.getCustomerAddress()[0] + " " + customer.getCustomerAddress()[1]+
                                " " + customer.getCustomerAddress()[2] + " " + customer.getCustomerAddress()[3];
               modeCustomerBill.insertRow(modeCustomerBill.getRowCount(),new Object[] {customer.getCustomerName(),address,
                                           customer.getCustomerTel()[0]});
     }

     public void CleanTable(DefaultTableModel table){
         table.getDataVector().removeAllElements();
         table.fireTableDataChanged();
     }

     public void AddProducts(String code, ArrayList<String> product ){
        String[] productArray = new String[product.size()];
        productArray = product.toArray(productArray);
         for(clsOrder ord:orderObjList ){
             System.out.println(Arrays.toString(ord.getProductList()));
             if(code.equals(ord.getCustomer().getCustomerCode())){
                 ord.setProductList(productArray);
            }
         }
     }
