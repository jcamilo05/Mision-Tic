    private void btnMakeOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMakeOrderActionPerformed
        String customerCode = txtCodeCustomer.getText();
        String date = txtDate.getText();
        if(customerObjList.isEmpty()){JOptionPane.showMessageDialog(this,"Please fill the fields first");}
        boolean flag = true;
        for(clsCustomer cust : customerObjList){
            if((cust.getCustomerCode()).equals(customerCode )){
                try{
                    clsOrder order = new clsOrder(date,cust);
                    orderObjList.add(order);
                    AddProducts(customerCode, products);
                    this.FillBillProducts(customerCode);
                    this.FillBillCustomer(cust);
                    JOptionPane.showMessageDialog(this, "The bill has been generated");
                    int total = order.getProductList().length;
                    txtTotal.setText(total + " products");
                    flag = false;
                    products.clear();

                    break;
                }catch(Exception e){
                    JOptionPane.showMessageDialog(this, e);
                }
            }
        }
        this.ClearBillPane();

        if(flag){JOptionPane.showMessageDialog(this, "The customer doesn't exist");}
    }//GEN-LAST:event_btnMakeOrderActionPerformed

