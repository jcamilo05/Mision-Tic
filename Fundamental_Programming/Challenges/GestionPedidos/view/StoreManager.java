        String street = cbStreetOrCarCustomer.getSelectedItem().toString();
        String number = txtNumberAdress.getText();
        String city = txtCustomerCity.getText();
        String nHood = txtNHoodCustomer.getText();
        String[] address = {street, number, city, nHood};

        customerObjList.get(index).setCustomerName(name);
